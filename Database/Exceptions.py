class DatabaseError(Exception):
    """Base class for exceptions in Database package."""
    pass

class PathError(DatabaseError):
    def __init__(self, path, msg=''):
        self.path = path
        self.msg = msg
        
    def __str__(self):
        return repr(self.path + ": " + self.msg)

